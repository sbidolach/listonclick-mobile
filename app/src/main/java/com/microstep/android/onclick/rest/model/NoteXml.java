package com.microstep.android.onclick.rest.model;

import java.util.ArrayList;
import java.util.List;

public class NoteXml {

    protected int id;
    protected String title;
    protected String type;
    protected String description;
    protected String dateCreated;
    protected String dateFinished;
    protected Integer identityId;
    protected Integer quantity;
    protected Double price;
    protected List<ProductXml> productXml;   

    public int getId() {
        return id;
    }

    public void setId(int value) {
        this.id = value;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String value) {
        this.title = value;
    }

    public String getType() {
        return type;
    }

    public void setType(String value) {
        this.type = value;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String value) {
        this.description = value;
    }	

	public String getDateCreated() {
		return dateCreated;
	}

	public void setDateCreated(String dateCreated) {
		this.dateCreated = dateCreated;
	}

	public String getDateFinished() {
		return dateFinished;
	}

	public void setDateFinished(String dateFinished) {
		this.dateFinished = dateFinished;
	}

	public Integer getIdentityId() {
        return identityId;
    }

    public void setIdentityId(Integer value) {
        this.identityId = value;
    }

    public List<ProductXml> getProductXml() {
        if (productXml == null) {
            productXml = new ArrayList<ProductXml>();
        }
        return this.productXml;
    }

	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

}
