package com.microstep.android.onclick.rest.model;

import java.math.BigInteger;
import java.util.ArrayList;
import java.util.List;

public class OrganizationXml {

	protected int id;
	protected String name;
	protected String description;
	protected String logoSmallUrl;
	protected String website;
	protected String dateCreated;
	protected String dateModified;
	protected BigInteger productSize;
	protected BigInteger promotionSize;
	protected BigInteger newsletterSize;
	protected List<LocationXml> locationXml = new ArrayList<LocationXml>();

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLogoSmallUrl() {
		return logoSmallUrl;
	}

	public void setLogoSmallUrl(String logoSmallUrl) {
		this.logoSmallUrl = logoSmallUrl;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getDateCreated() {
		return dateCreated;
	}

	public void setDateCreated(String dateCreated) {
		this.dateCreated = dateCreated;
	}

	public String getDateModified() {
		return dateModified;
	}

	public void setDateModified(String dateModified) {
		this.dateModified = dateModified;
	}

	public List<LocationXml> getLocationXml() {
		return locationXml;
	}

	public void setLocationXml(List<LocationXml> locationXml) {
		this.locationXml = locationXml;
	}

	public BigInteger getProductSize() {
		return productSize;
	}

	public void setProductSize(BigInteger productSize) {
		this.productSize = productSize;
	}

	public BigInteger getPromotionSize() {
		return promotionSize;
	}

	public void setPromotionSize(BigInteger promotionSize) {
		this.promotionSize = promotionSize;
	}

	public BigInteger getNewsletterSize() {
		return newsletterSize;
	}

	public void setNewsletterSize(BigInteger newsletterSize) {
		this.newsletterSize = newsletterSize;
	}

}
